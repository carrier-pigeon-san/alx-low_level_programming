/**
 * add - adds two given numbers
 * @a: first number
 * @b: Secod number
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - substracts one number from another
 * @a: first number
 * @b: Secod number
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two given numbers
 * @a: first number
 * @b: Secod number
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two given numbers
 * @a: first number
 * @b: Secod number
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - returns remainder of division between two numbers
 * @a: first number
 * @b: Secod number
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}

